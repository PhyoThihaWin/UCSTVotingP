package com.vote.queen.king.ucstvoting.model;

public class Agenda {
    String txt1, txt2;

    public Agenda() {
    }

    public Agenda(String txt1, String txt2) {
        this.txt1 = txt1;
        this.txt2 = txt2;
    }

    public String getTxt1() {
        return this.txt1;
    }

    public void setTxt1(String txt1) {
        this.txt1 = txt1;
    }

    public String getTxt2() {
        return this.txt2;
    }

    public void setTxt2(String txt2) {
        this.txt2 = txt2;
    }


}
